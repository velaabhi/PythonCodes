str = 'HelloWorld !' #string is enclosed in Quotation marks

print (str) # Prints complete string
print (str[0]) # Prints first character of the string, [] & [:] are slice operators
print(str[2:6])  # Prints characters starting from indx 2 to 5 "lloW", 6 not taken
print (str[2:]) # Prints characters starting from indx 2 till end
print (str*2)   # (*) operator is used for repetition purpose
print (str+"test") #(+) operator is used for concat purpose